public class Radius {
    
    private String lat;
    private String lng;
    private String rad;

    public String getLat() {
        return lat;
    }

    public String getLng() {
        return lng;
    }

    public String getRad() {
        return rad;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public void setRad(String rad) {
        this.rad = rad;
    }
    
    
}
